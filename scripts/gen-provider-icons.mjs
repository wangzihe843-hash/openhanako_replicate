// 一次性工具：从 @lobehub/icons-static-svg (MIT) 拉取单色 provider logo，
// 并合并本地核验过的 SVG，内联生成 desktop/src/react/ui/provider-icons.ts。
// ProviderIcon 运行时只读取生成后的 TS 数据，保持零运行时依赖。
// 新增 provider 时在 ICONS 里加 lobehub 图标名后重跑：node scripts/gen-provider-icons.mjs
import { readFileSync, writeFileSync } from 'node:fs';

// lobehub 图标名（已逐个核验存在）。ProviderIcon 负责把 provider id 映射到这些 key。
const ICONS = [
  'deepseek', 'qwen', 'openai', 'gemini', 'volcengine', 'moonshot',
  'kimi', 'zhipu', 'siliconcloud', 'groq', 'mistral', 'minimax',
  'openrouter', 'xiaomimimo', 'ollama',
];

// 本地图标源：用于 lobehub 未覆盖或需要项目内固定来源的 provider。
const LOCAL_ICONS = {
  fireworks: 'scripts/provider-icons/fireworks.svg',
};

function parseSvg(name, svg) {
  const viewBox = svg.match(/viewBox="([^"]*)"/)?.[1] || '0 0 24 24';
  const content = svg
    .replace(/^[\s\S]*?<svg[^>]*>/, '')
    .replace(/<\/svg>\s*$/, '')
    .replace(/<title>[\s\S]*?<\/title>/, '')
    .replace(/\sfill="(?!none\b)[^"]*"/g, '')
    .trim();
  if (!content.includes('<path') && !content.includes('<g')) {
    throw new Error(`${name}: unexpected svg content -> ${content.slice(0, 80)}`);
  }
  return { viewBox, content };
}

const out = {};
for (const name of ICONS) {
  const res = await fetch(`https://unpkg.com/@lobehub/icons-static-svg/icons/${name}.svg`);
  if (!res.ok) throw new Error(`fetch ${name} failed: ${res.status}`);
  const svg = await res.text();
  out[name] = parseSvg(name, svg);
  console.log(`✓ ${name} (${out[name].content.length} chars)`);
}

for (const [name, filePath] of Object.entries(LOCAL_ICONS)) {
  const svg = readFileSync(filePath, 'utf8');
  out[name] = parseSvg(name, svg);
  console.log(`✓ ${name} (${out[name].content.length} chars, local)`);
}

const header = '// Auto-generated by scripts/gen-provider-icons.mjs from @lobehub/icons (MIT) and local SVG sources.\n'
  + '// Single-color provider logos (fill: currentColor). Do not edit by hand.\n\n';
writeFileSync(
  'desktop/src/react/ui/provider-icons.ts',
  header
    + 'export const PROVIDER_ICONS: Record<string, { viewBox: string; content: string }> = '
    + JSON.stringify(out, null, 2) + ';\n',
);
console.log(`\nGenerated ${Object.keys(out).length} icons -> desktop/src/react/ui/provider-icons.ts`);
