/**
 * 「小手机报纸」按 era 分化的笔调描述符。
 *
 * 与 xingye-news-era-resolver.ts 配套：resolver 算出 NewsEraId，本文件提供
 * 注入 prompt 的笔调指南。各 era 对应一种「报刊年代」：
 *  - oriental_classical → 民国小报闲笔体（文白夹杂；覆盖 中国古代 / 武侠仙侠 / 民国）
 *  - western_fantasy    → 早期西方八卦小报译文体（17-19 世纪 broadsheet / gossip rag 的翻译质感）
 *  - modern_or_future   → 现代狗仔小报体（明报 / 苹果 / 微博狗仔 / 知名爆料账号；废土科幻沿用骨架）
 *
 * 之所以三选一而不更细分（古风 vs 民国 vs 仙侠 vs 西幻 vs 中世纪 …），是因为
 * 「报刊年代」本身就是一个粗粒度概念——把仙侠和民国统一到「小报闲笔体」、
 * 把蒸朋和中世纪统一到「译文体」，对 LLM 来说指令更清晰、不易跳戏；细分到
 * 「该写哪个朝代风物」的层面由 lore 自己驱动。
 */

import type { NewsEraId } from './xingye-news-era-resolver';

export type NewsEraStyleDescriptor = {
  /** 人类可读的笔调名（写进 prompt，让模型自报家门）。 */
  toneName: string;
  /** 一句话总结（写进 prompt 头部"笔调与报刊年代"开头）。 */
  toneSummary: string;
  /**
   * 主体写作守则。多行，会按行拼进 prompt。逐条用「-」开头，避免模型按段落拼字数。
   * 内容覆盖：句法 / 用词 / 称谓 / 引语方式 / 节奏。
   */
  writingStyleGuide: readonly string[];
  /**
   * 标题风格。多行，描述本 era 的标题该怎么取——三段式 / 对仗 / 长标题党 ……
   * 模型最终选用的板块标题仍来自 NEWS_SECTION_REGISTRY.titleCandidates；这里只是
   * 调正文里嵌入的副标题、headline 引语等的笔调。
   */
  headlineStyleGuide: readonly string[];
  /**
   * masthead（报头）命名风格。 prompt 端会把这段塞进「masthead」说明里，替代
   * 通用的"古代用《暮报》/ 未来用《辐尘日报》"那一行。
   * exampleMastheads 给模型几个**仿名样本**——不是必须选用，只是定调。
   */
  mastheadStyleGuide: string;
  exampleMastheads: readonly string[];
  /**
   * byline（虚构记者 / 笔名）风格。
   * exampleBylines 同样只是定调用的样本。
   */
  bylineStyleGuide: string;
  exampleBylines: readonly string[];
  /**
   * 笔调专属的硬禁忌词 / 禁忌写法。每行一条，会以「- 禁用：…」形式出现在 prompt 里。
   * 主要防穿越元素（民国小报里冒出"微博热搜"、狗仔体里冒出"业经传闻"）。
   */
  taboos: readonly string[];
};

